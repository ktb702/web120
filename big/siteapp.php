        <h2>Responsive Websites vs Mobile Apps</h2>
        <p>With the amount of smartphone users in the world, and the amount of time they spend on their phones,
        one might think that mobile apps are hands down the way to go. According to eMarketer, “The average adult 
        will spend 2 hours, 25 minutes per day using mobile apps”, but “time spent on a mobile browser is only 26
        minutes per day”¹.  But mobile apps might not always be the best choice for your business. The question
        isn’t “Which one is better?”, the question is “Which one is right for you?”. To answer this question, 
        let’s take a look at some of the advantages and disadvantages of each.</p>

        <p>First let’s think about the user base. Companies with a large user base will have an easier time 
        convincing people to download and install their app. According to Sveta Cherednihenko, “as the number 
        of mobile apps has reached over four million on the App Store and Google Play Store combined, it’s a 
        challenge to get people to download new apps”². For a startup company, it could be quite difficult to 
        build a user base with only a mobile app. Responsive websites will also appear in internet searches, 
        making it easier to drive new customers to your site. </p>

        <p>Next let’s consider performance. Since mobile apps are created for specific operating systems 
        (like iOS or Android) and downloaded to the device, they are much faster. Mobile apps can also send push 
        notifications and have access to other features like the device’s camera, gps, or phone dialing. They 
        can be used offline as well. But they are also platform specific, and responsive websites will work on 
        any device. </p>

        <p>Finally let’s consider the bottom line. Responsive websites require less investment of time and 
        money to create. They are also easier to update and maintain. Updates to websites are available to their 
        users immediately. Updating a mobile app is much more complicated. Multiple updates are required (one for 
        each platform) and then the update needs approval from the app markets before it becomes available to 
        users. It’s important to note that this approval process can take several days. </p>

        <p>Choosing a responsive website or a mobile app (or even both) just depends on the needs of your business, 
        so make sure to consider all factors when making a decision. </p>
