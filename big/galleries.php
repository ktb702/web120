        <h2>Galleries</h2>
        <p>With the millions of websites in existence today, you have to make your website stand out in 
            order to attract new users. A visually appealing website is crucial. When a user visits a 
            website they will often exit immediately if the content is hard to read, the layout is hard to 
            follow, or there is just a giant wall of text. A great way to break up the text on your site and 
            grip your target audience is by utilizing galleries. “Research has shown that when we engage with information that has a visual element attached to it,  we are likely to retain 65% of it 3 days later.
        This, as opposed to a mere 10% of the information when it is lacking in accompanied imagery.”¹
        </p>

        <p>There are countless tools, templates, and tutorials that can be used to add these visual elements and galleries to your site, many of them can be found online for free. One handy tool is lightbox, created by Lokesh Dhakar, and the code for this tool is available on github. Content management systems (like WordPress) can also be used to display images or galleries. But the one that is rising in popularity is the use of jQuery sliders.</p>

        <p>There are lots of different styles available for jQuery sliders. A standard/basic photo slider is a good place to start. If you want to include more than just photos, there are mixed media sliders. There are also several different types of 3D sliders and tons of transition effects that can be used to create more stunning visual effects. You can even design your own slider. The possibilities are really endless. As CreativesFeed puts it, “A slider is like a mustache-mullet combo; do it wrong and you look like an out-of-date tool, do it right and you’re a legend.”² </p>

